// 큰 서브 6
$("#checkbox-success").change(function () {
  if ($("#checkbox-success").is(":checked")) {
    if (window.localStorage.getItem("subGoalNum") == 6_1) {
      window.localStorage.setItem("sub6-1-success", "on"); // 백에 true 값으로 전달
      const alarmDiv = document.createElement("div");
      alarmDiv.className = "alarm";
      alarmDiv.id = "alarm-1";
      goal1.appendChild(alarmDiv);
    }
  } else {
    if (window.localStorage.getItem("subGoalNum") == 6_1) {
      window.localStorage.setItem("sub6-1-success", "off"); // 백에 false 값으로 전달
      document.getElementById("alarm-1").remove();
    }
  }
});
if (
  window.localStorage.getItem("subGoalNum") == 6_1 &&
  window.localStorage.getItem("sub6-1-success") == "on"
) {
  $("#checkbox-success").prop("checked", true);
}

